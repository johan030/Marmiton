use Doctrine\Persistence\ManagerRegistry;

/**
 * @method Recette|null find($id, $lockMode = null, $lockVersion = null)
 * @method Recette|null findOneBy(array $criteria, array $orderBy = null)
 * @method Recette[]    findAll()
 * @method Recette[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class RecetteRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Recette::class);
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(Recette $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(Recette $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }
    public function search($value)
    {   //SELECT * FROM post as l WHERE l.titre LIKE "%xxx%" ORDER BY l.titre, l.auteur
        return $this->createQueryBuilder('p')//le paramètre l représente la table post (comme un alias dans une requête SQL)
            ->where('p.title LIKE :val')
            ->setParameter('val', "%$value%")
            ->orderBy('p.title', 'ASC')
            ->getQuery()
            ->getResult()
        ;
    }
